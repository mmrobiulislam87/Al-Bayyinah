def global_index_to_surah_ayah(global_idx: int) -> tuple[int, int]:
    """1-based global ayah index (1..6236) -> (surah, ayah)."""
    if global_idx < 1:
        raise ValueError("global_idx must be >= 1")
    remaining = global_idx
    for si, cnt in enumerate(SURAH_AYAH_COUNTS, start=1):
        if remaining <= cnt:
            return si, remaining
        remaining -= cnt
    raise ValueError("global_idx exceeds total ayah count")


def total_ayahs() -> int:
    return sum(SURAH_AYAH_COUNTS)
